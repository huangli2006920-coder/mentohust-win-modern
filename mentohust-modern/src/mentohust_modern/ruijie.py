@dataclass(slots=True)
class RuntimeNetworkConfig:
    ipaddr: str
    gateway: str
    mask: str
    dns: str
    ping_ip: str
    local_mac: str


def encode_byte(value: int) -> int:
    result = 0
    for _ in range(8):
        result <<= 1
        result |= value & 0x01
        value >>= 1
    return (~result) & 0xFF


def checksum_fill(buffer: bytearray) -> None:
    checksum = [buffer[0x15], buffer[0x16]]
    for index in range(0x15):
        table_index = checksum[0] ^ buffer[index]
        checksum[0] = checksum[1] ^ CHECKSUM_TABLE[table_index * 2 + 1]
        checksum[1] = CHECKSUM_TABLE[table_index * 2]
    buffer[0x15] = checksum[0]
    buffer[0x16] = checksum[1]
    for index in range(0x17):
        buffer[index] = encode_byte(buffer[index])


def parse_version(value: str) -> tuple[int, int]:
    major, _, minor = value.partition(".")
    return int(major or "0"), int(minor or "0")


def get_version_from_exe(exe_path: str | Path) -> tuple[int, int]:
    pe = pefile.PE(str(exe_path))
    fixed = pe.VS_FIXEDFILEINFO[0]
    return fixed.FileVersionMS >> 16, fixed.FileVersionMS & 0xFFFF


def read_code_section(exe_path: str | Path) -> bytes:
    pe = pefile.PE(str(exe_path))
    for section in pe.sections:
        if section.Characteristics & 0x20000000 and section.Characteristics & 0x00000020:
            return bytes(section.get_data())
    raise ValueError(f"No executable code section found in {exe_path}")


def compute_v2_check(seed: bytes, exe_path: str | Path) -> str:
    code = read_code_section(exe_path)
    size = len(code) // 8
    table = bytearray(144)
    for index in range(8):
        chunk = seed + code[index * size : (index + 1) * size]
        digest = hashlib.md5(chunk).digest()
        table[index * 18] = seed[index * 2]
        table[index * 18 + 1 : index * 18 + 17] = digest
        table[index * 18 + 17] = seed[index * 2 + 1]
    return hashlib.md5(table).hexdigest().upper()


def _set_property(fill_buf: bytearray, type_id: int, value: bytes) -> None:
    pointer = 0x46
    end = len(fill_buf) - len(value) - 8
    while pointer < end:
        if fill_buf[pointer] == 0x1A:
            pointer += 2
        if fill_buf[pointer + 4] == type_id:
            start = pointer + 4 + fill_buf[pointer + 5] - len(value)
            fill_buf[start : start + len(value)] = value
            return
        pointer += fill_buf[pointer + 5] + 4
    raise ValueError(f"Property 0x{type_id:02x} not found in vendor buffer")


def _ethernet_prefix(dest_mac: bytes, local_mac: bytes, protocol: bytes) -> bytearray:
    packet = bytearray(0x3E8)
    packet[0:6] = dest_mac
    packet[6:12] = local_mac
    packet[12:16] = protocol
    return packet


def _make_cernet_start(local_mac: bytes) -> bytes:
    packet = bytearray(60)
    packet[0:6] = STANDARD_ADDR
    packet[6:12] = local_mac
    packet[12:16] = bytes.fromhex("88 8e 01 01")
    packet[16:18] = b"\x00\x00"
    packet[18:60] = b"\xa5" * 42
    return bytes(packet)


def _make_cernet_logoff(dest_mac: bytes, local_mac: bytes, code: bytes) -> bytes:
    packet = bytearray(60)
    packet[0:6] = dest_mac
    packet[6:12] = local_mac
    packet[12:16] = bytes.fromhex("88 8e 01 02")
    packet[16:18] = b"\x00\x00"
    packet[18:60] = b"\xa5" * 42
    return bytes(packet)


def build_fill_buffer(
    config: MentohustConfig,
    runtime: RuntimeNetworkConfig,
    dhcp_mode_runtime: int,
) -> bytearray:
    use_windows_template = config.version != "0.00"
    fill_size = 0x1D7 if use_windows_template else 0x80
    fill_buf = bytearray(fill_size)
    fill_buf[0x02] = 0x13
    fill_buf[0x03] = 0x11
    if dhcp_mode_runtime != 0:
        fill_buf[0x04] = 0x01
    fill_buf[0x05:0x09] = socket.inet_aton(runtime.ipaddr)
    fill_buf[0x09:0x0D] = socket.inet_aton(runtime.mask)
    fill_buf[0x0D:0x11] = socket.inet_aton(runtime.gateway)
    fill_buf[0x11:0x15] = socket.inet_aton(runtime.dns)
    checksum_fill(fill_buf)
    template = WINDOWS_TEMPLATE if use_windows_template else XRGSU_TEMPLATE
    fill_buf[0x17 : 0x17 + len(template)] = template

    if use_windows_template:
        major, minor = parse_version(config.version)
        fill_buf[0x3B] = major & 0xFF
        fill_buf[0x3C] = minor & 0xFF

    dhcp_first = b"\x01" if dhcp_mode_runtime == 1 else b"\x00"
    local_mac = bytes.fromhex(runtime.local_mac.replace(":", ""))
    _set_property(fill_buf, 0x18, dhcp_first)
    _set_property(fill_buf, 0x2D, local_mac)
    return fill_buf


def apply_md5_check(fill_buf: bytearray, md5_seed: bytes, config: MentohustConfig) -> None:
    client_exe = config.client_exe_path()
    if not client_exe.exists():
        return
    _set_property(fill_buf, 0x17, compute_v2_check(md5_seed, client_exe).encode("ascii"))


def compute_password_md5(identifier: int, password: str, md5_seed: bytes, cernet: bool) -> bytes:
    payload = bytearray([identifier])
    payload.extend(password.encode("utf-8"))
    if cernet:
        payload.extend(b"xxghlmxhzb")
    payload.extend(md5_seed)
    return hashlib.md5(payload).digest()


def build_start_packet(fill_buf: bytes, local_mac: str, dest_mac: bytes, mode: int) -> bytes:
    local_mac_bytes = bytes.fromhex(local_mac.replace(":", ""))
    if mode == 2:
        return _make_cernet_start(local_mac_bytes)
    packet = _ethernet_prefix(dest_mac, local_mac_bytes, bytes.fromhex("88 8e 01 01"))
    packet[0x12 : 0x12 + len(fill_buf)] = fill_buf
    return bytes(packet)


def build_identity_packet(fill_buf: bytes, local_mac: str, dest_mac: bytes, server_packet: bytes, username: str, mode: int) -> bytes:
    local_mac_bytes = bytes.fromhex(local_mac.replace(":", ""))
    user_bytes = username.encode("utf-8")
    if mode == 2:
        packet = bytearray(48 + len(user_bytes))
        packet[0:6] = dest_mac
        packet[6:12] = local_mac_bytes
        packet[12:14] = bytes.fromhex("88 8e")
        packet[14:16] = bytes.fromhex("01 00")
        total = len(user_bytes) + 30
        packet[16:18] = total.to_bytes(2, "big")
        packet[20:22] = total.to_bytes(2, "big")
        packet[18] = 0x02
        packet[19] = server_packet[0x13]
        packet[22] = 0x01
        packet[23] = 0x01
        packet[24:28] = server_packet[24:28]
        packet[28:32] = server_packet[28:32]
        packet[32:36] = server_packet[32:36]
        packet[40:48] = b"03.02.05"
        packet[48 : 48 + len(user_bytes)] = user_bytes
        return bytes(packet)
    packet = _ethernet_prefix(dest_mac, local_mac_bytes, bytes.fromhex("88 8e 01 00"))
    total = len(user_bytes) + 5
    packet[16:18] = total.to_bytes(2, "big")
    packet[20:22] = total.to_bytes(2, "big")
    packet[18] = 0x02
    packet[19] = server_packet[0x13]
    packet[22] = 0x01
    packet[23 : 23 + len(user_bytes)] = user_bytes
    packet[23 + len(user_bytes) : 23 + len(user_bytes) + len(fill_buf)] = fill_buf
    return bytes(packet)


def build_challenge_packet(
    fill_buf: bytes,
    local_mac: str,
    dest_mac: bytes,
    server_packet: bytes,
    username: str,
    password: str,
    mode: int,
) -> bytes:
    local_mac_bytes = bytes.fromhex(local_mac.replace(":", ""))
    user_bytes = username.encode("utf-8")
    seed_length = server_packet[0x17]
    md5_seed = server_packet[0x18 : 0x18 + seed_length]
    password_md5 = compute_password_md5(server_packet[0x13], password, md5_seed, cernet=(mode == 2))
    if mode == 2:
        packet = bytearray(40 + len(user_bytes))
        packet[0:6] = dest_mac
        packet[6:12] = local_mac_bytes
        packet[12:14] = bytes.fromhex("88 8e")
        packet[14:16] = bytes.fromhex("01 00")
        total = len(user_bytes) + 22
        packet[16:18] = total.to_bytes(2, "big")
        packet[20:22] = total.to_bytes(2, "big")
        packet[18] = 0x02
        packet[19] = server_packet[0x13]
        packet[22] = 0x04
        packet[23] = 16
        packet[24:40] = password_md5
        packet[40 : 40 + len(user_bytes)] = user_bytes
        return bytes(packet)
    packet = _ethernet_prefix(dest_mac, local_mac_bytes, bytes.fromhex("88 8e 01 00"))
    total = len(user_bytes) + 22
    packet[16:18] = total.to_bytes(2, "big")
    packet[20:22] = total.to_bytes(2, "big")
    packet[18] = 0x02
    packet[19] = server_packet[0x13]
    packet[22] = 0x04
    packet[23] = 16
    packet[24:40] = password_md5
    packet[40 : 40 + len(user_bytes)] = user_bytes
    packet[40 + len(user_bytes) : 40 + len(user_bytes) + len(fill_buf)] = fill_buf
    return bytes(packet)


def extract_echo_key(packet: bytes) -> int:
    offset = 0x1C + packet[0x1B] + 0x69 + 24
    reversed_bytes = packet[offset : offset + 4][::-1]
    encoded = bytes(encode_byte(value) for value in reversed_bytes)
    return int.from_bytes(encoded, "little")


def fill_echo_fields(packet: bytearray, echo_key: int, echo_no: int) -> None:
    bt1 = (echo_key + echo_no).to_bytes(4, "big")
    bt2 = echo_no.to_bytes(4, "big")
    for index in range(4):
        packet[0x18 + index] = encode_byte(bt1[index])
        packet[0x22 + index] = encode_byte(bt2[index])


def build_echo_packet(local_mac: str, dest_mac: bytes, echo_key: int, echo_no: int, mode: int) -> bytes:
    local_mac_bytes = bytes.fromhex(local_mac.replace(":", ""))
    if mode == 2:
        packet = bytearray(60)
        packet[0:6] = dest_mac
        packet[6:12] = local_mac_bytes
        packet[12:16] = bytes.fromhex("88 8e 01 06")
        packet[16:18] = b"\x00\x00"
        packet[18:60] = b"\xa5" * 42
        return bytes(packet)
    packet = _ethernet_prefix(dest_mac, local_mac_bytes, bytes.fromhex("88 8e 01 bf"))
    packet[16:16 + len(ECHO_TEMPLATE)] = ECHO_TEMPLATE
    fill_echo_fields(packet, echo_key, echo_no)
    return bytes(packet[:0x2D])


def build_logoff_packet(fill_buf: bytes, local_mac: str, dest_mac: bytes, mode: int) -> bytes:
    local_mac_bytes = bytes.fromhex(local_mac.replace(":", ""))
    if mode == 2:
        return _make_cernet_logoff(dest_mac, local_mac_bytes, fill_buf)
    packet = _ethernet_prefix(dest_mac, local_mac_bytes, bytes.fromhex("88 8e 01 02"))
    packet[0x12 : 0x12 + len(fill_buf)] = fill_buf
    return bytes(packet)


def parse_server_messages(packet: bytes) -> list[str]:
    messages: list[str] = []
    first_len = packet[0x1B]
    if first_len:
        raw = packet[0x1C : 0x1C + first_len].lstrip(b"\r\n ")
        if raw:
            messages.append(raw.decode("gbk", errors="ignore").strip())
    second_offset = 0x1C + packet[0x1B] + 0x69 + 39
    if second_offset < len(packet):
        second_size = packet[second_offset - 1] - 2
        if second_size > 0 and second_offset + second_size <= len(packet):
            raw = packet[second_offset : second_offset + second_size].lstrip(b"\r\n ")
            if raw:
                messages.append(raw.decode("gbk", errors="ignore").strip())
    return [value for value in messages if value]
